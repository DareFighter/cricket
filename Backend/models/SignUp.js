const mongoose = require("mongoose");

const SignUpSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  confirmPassword: {
    type: String,
    required: true,
  },
});

const SignUpDetails = mongoose.model("SignUp", SignUpSchema);

module.exports = SignUpDetails;
